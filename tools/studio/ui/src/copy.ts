// Human labels for the technical provider types: the <select> shows these, the value stays the
// type. `satisfies` makes a missing label a compile error if a provider type is ever added. The
// labels are the same in both languages (brand and protocol names), so one map serves both.
const PROVIDER_TYPE_LABELS = {
  "openai-compatible": "OpenAI / compatible",
  ollama: "Ollama (local)",
  anthropic: "Anthropic",
  google: "Google",
} satisfies Record<ProviderSettings["type"], string>;

// Actionable text for each base-llm failure code (packages/base-llm/src/errors.mjs), per language.
// Open for extension: a new failure mode is one more entry, never a change to the rendering control
// flow; an unknown code falls back to the raw server message at the call site, so nothing is hidden.
export const PROVIDER_ERRORS: Record<string, (env?: string) => string> = {
  "llm.config": (env) => `clé absente: exportez ${env ?? "la variable d'environnement"} dans le terminal qui lance «base studio», puis relancez`,
  "llm.auth": (env) => `clé refusée: vérifiez la valeur de ${env ?? "la variable d'environnement"} côté serveur`,
  "llm.rate_limit": () => "trop de requêtes pour le moment: réessayez dans un instant",
  "llm.network": () => "serveur injoignable: vérifiez l'URL et que le service tourne",
  "llm.timeout": () => "délai dépassé: le serveur a mis trop de temps à répondre",
  "llm.response": () => "réponse inattendue du fournisseur",
};

const PROVIDER_ERRORS_EN: Record<string, (env?: string) => string> = {
  "llm.config": (env) => `key missing: export ${env ?? "the environment variable"} in the terminal that runs "base studio", then restart`,
  "llm.auth": (env) => `key rejected: check the value of ${env ?? "the environment variable"} on the server side`,
  "llm.rate_limit": () => "too many requests right now: try again in a moment",
  "llm.network": () => "server unreachable: check the URL and that the service is running",
  "llm.timeout": () => "timed out: the server took too long to respond",
  "llm.response": () => "unexpected response from the provider",
};

// Map a coded provider failure to its actionable text in the given language, falling back to the raw
// server message when the code is unknown (so nothing is hidden). Shared by the Réglages connection
// test and the chat error path, so the same failure reads the same way wherever it surfaces.
export function providerErrorText(r: { code?: string; env?: string; error?: string }, lang: Lang = "fr"): string {
  const table = lang === "en" ? PROVIDER_ERRORS_EN : PROVIDER_ERRORS;
  const render = r.code ? table[r.code] : undefined;
  return render ? render(r.env) : r.error ?? (lang === "en" ? "unknown error" : "erreur inconnue");
}

