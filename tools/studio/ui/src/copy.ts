// One source of truth for the Réglages screen, the model picker, and the editor fields, in both UI
// languages. Plain values plus a few small formatters. `copyFor(lang)` selects a locale; `useCopy()`
// reads the active language from LangContext (i18n.ts). `COPY` stays as a French alias so a component
// not yet migrated to useCopy() keeps working (English is added screen by screen).
//
// French strings follow the BASE conventions: tight Swiss-romand punctuation (no space before
// : ; ! ?), tight guillemets «…», no em-dash. English strings use straight quotes and American
// spelling. They are user-facing but live in code, so the .md docs gates do not see them: keep them
// clean by hand.

