const CATALOG: Record<Lang, typeof FR> = { fr: FR, en: EN };

/** The copy catalog for a language. */
export function copyFor(lang: Lang): typeof FR {
  return CATALOG[lang];
}

/** The copy catalog for the active UI language (from LangContext). */
export function useCopy(): typeof FR {
  return copyFor(useLang());
}

